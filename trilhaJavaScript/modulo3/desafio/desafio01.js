var array = [ 2, 3, 5, 7, 11, 13, 18, 34 ];

// Percorre a array usando um loop for
for (var i = 0; i < array.length; i++) {
    // Verifica se o número na posição atual é par
    if (array[i] % 2 === 0) {
        // Se for par, imprime o número
        console.log(array[i]);
    }
}

